    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDisease.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select the row to delete the Disease from the System", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            Disease p = (Disease) tblDisease.getValueAt(selectedRow, 0);

            business.getDiseaseDirectory().removeDisease(p);
            JOptionPane.showMessageDialog(null, "You have successfully deleted the Disease from the System");
            populateDiseaseTable();
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

