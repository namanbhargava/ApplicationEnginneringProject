    public void populateDiseaseTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblDisease.getModel();
        dtm.setRowCount(0);
        for (Disease disease : business.getDiseaseDirectory().getDiseaseList()) {
            Object[] row = new Object[1];
            row[0] = disease;
            dtm.addRow(row);
        }
    }

