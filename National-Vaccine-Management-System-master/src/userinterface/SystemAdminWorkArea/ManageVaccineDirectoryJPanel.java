    public void populateVaccineTable(){
         DefaultTableModel dtm= (DefaultTableModel) tblVaccine.getModel();
       dtm.setRowCount(0);
       for (Vaccine vaccine : business.getVaccineDirectory().getVaccineList()) {        
           Object[] row = new Object[3];
           row[0]= vaccine;
           row[1]=vaccine.getVaccineId();
           row[2]= vaccine.getPrice();
           dtm.addRow(row);
       }  
       }
    
    public void populatecombo(){
        for (Disease disease : business.getDiseaseDirectory().getDiseaseList()) {
            comboDisease.addItem(disease);
        }
    }

