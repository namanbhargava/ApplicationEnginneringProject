    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
         int selectedRow= tblVaccine.getSelectedRow();
         if(selectedRow<0)
         {
             JOptionPane.showMessageDialog(null, "Please select the Vaccine to be Deleted from above Grid.", "Warning", JOptionPane.WARNING_MESSAGE);
         }
        else
         {
             
         Vaccine p=(Vaccine) tblVaccine.getValueAt(selectedRow, 0);

         business.getVaccineDirectory().removeVaccine(p);
         
          JOptionPane.showMessageDialog(null, "You have successfully deleted the Vaccine");
        populateVaccineTable();
         }
         
    }//GEN-LAST:event_btnDeleteActionPerformed

